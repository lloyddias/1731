package Pojo;

public class Ball {
	private int size;
	private int weight;
	
	
	public Ball(int size, int weight) {
		super();
		this.size = size;
		this.weight = weight;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}

}
